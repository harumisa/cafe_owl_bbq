  <script>
    $(function() {
      $('.syutsugen1, .syutsugen2, .syutsugen3').on('inview', function(event, isInView) {
        if (isInView) {
          $(this).addClass('syutsugen-fix');
        };
      });

      $('.content-title1, .content-title2, .content-title3').on('inview', function(event, isInView) {
        if (isInView) {
          $(this).find('.content-title-underline').addClass('content-title-underline-fix');
        };
      });

      $('.step').on('inview', function(event, isInView) {
        if (isInView) {
          $('.step').each(function(i){
            var delay = 500;
            $(this).delay(i * delay).queue(function(next) {
              $(this).addClass('step-fix');
              next();
            });
          });
        };
      });

      $('#jumpscroll1').on('click', function() {
        var jumpposition1Top = $('#jumpposition1').offset().top;
        $('html').animate({scrollTop: jumpposition1Top});
      });

      $('#jumpscroll2').on('click', function() {
        var jumpposition2Top = $('#jumpposition2').offset().top;
        $('html').animate({scrollTop: jumpposition2Top});
      });

      $('.headermenu-item1, .headermenu-item2, .headermenu-item3, .headermenu-item4').mouseover(function() {
        $(this).find('.headermenu-item-underline').addClass('headermenu-item-underline-fix');
      }).mouseout(function() {
        $(this).find('.headermenu-item-underline').removeClass('headermenu-item-underline-fix');
      });

      $(window).on('scroll', function() {
        if ( 500 < $(this).scrollTop() ) {
          $('#jumptotop').slideDown('fast');
        } else {
          if ($('#jumptotop').css('display') == 'block') {
            $('#jumptotop').slideUp('fast');
          };
        };
      });
      $('#jumptotop').on('click', function() {
        $('html').animate({scrollTop: 0});
      });
    });
  </script>
